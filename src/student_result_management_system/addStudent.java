    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
     String course_name = (String)jComboBox1.getSelectedItem();
String branch_name = (String)jComboBox2.getSelectedItem();
String gender = (String)jComboBox3.getSelectedItem();
String roll_number = jTextField1.getText();
String student_name = jTextField2.getText();
String father_name = jTextField3.getText();
String address = jTextField4.getText();

try {
    Class.forName("com.mysql.jdbc.Driver");
    // Establishing database connection
    Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "root");

    // Check if the roll number already exists in the database
    if (isRollNumberAlreadyExists(connection, roll_number)) {
        JOptionPane.showMessageDialog(null, "Student with the same roll number already exists.", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        // SQL query to insert data into the 'student' table
        String query = "INSERT INTO student (course_name, branch_name, roll_number, student_name, gender, father_name, address) VALUES (?, ?, ?, ?, ?, ?, ?)";

        // Creating a PreparedStatement to safely insert data
        PreparedStatement preparedStatement = connection.prepareStatement(query);
        preparedStatement.setString(1, course_name);
        preparedStatement.setString(2, branch_name);
        preparedStatement.setString(3, roll_number);
        preparedStatement.setString(4, student_name);
        preparedStatement.setString(5, gender);
        preparedStatement.setString(6, father_name);
        preparedStatement.setString(7, address);

        // Executing the query
        int rowsAffected = preparedStatement.executeUpdate();

        // Check if the data was inserted successfully
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Student registration successful.");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to register student.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    // Close the database connection
    connection.close();
} catch (SQLException | ClassNotFoundException e) {
}
        
    }//GEN-LAST:event_jButton7ActionPerformed
private boolean isRollNumberAlreadyExists(Connection connection, String rollNumber) throws SQLException {
    String query = "SELECT * FROM student WHERE roll_number = ?";
    try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
        preparedStatement.setString(1, rollNumber);
        try (ResultSet resultSet = preparedStatement.executeQuery()) {
            return resultSet.next(); // Returns true if there is at least one record with the given roll number
        }
    }
}

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
         String course_name = (String)jComboBox1.getSelectedItem();
    String branch_name = (String)jComboBox2.getSelectedItem();
    String gender = (String)jComboBox3.getSelectedItem();
    String roll_number = jTextField1.getText();
    String student_name = jTextField2.getText();
    String father_name = jTextField3.getText();
    String address = jTextField4.getText();

    try {
        Class.forName("com.mysql.jdbc.Driver");
        // Establishing database connection
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "root");

        // Check if the roll number exists in the database for editing
        if (!isRollNumberAlreadyExists(connection, roll_number)) {
            JOptionPane.showMessageDialog(null, "Student with the given roll number does not exist.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // SQL query to update data in the 'student' table
            String query = "UPDATE student SET course_name=?, branch_name=?, student_name=?, gender=?, father_name=?, address=? WHERE roll_number=?";

            // Creating a PreparedStatement to safely update data
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, course_name);
            preparedStatement.setString(2, branch_name);
            preparedStatement.setString(3, student_name);
            preparedStatement.setString(4, gender);
            preparedStatement.setString(5, father_name);
            preparedStatement.setString(6, address);
            preparedStatement.setString(7, roll_number);

            // Executing the update query
            int rowsAffected = preparedStatement.executeUpdate();

            // Check if the data was updated successfully
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Student details updated successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to update student details.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

        // Close the database connection
        connection.close();
    } catch (SQLException | ClassNotFoundException e) {
        e.printStackTrace();
    }
                

    }//GEN-LAST:event_jButton11ActionPerformed


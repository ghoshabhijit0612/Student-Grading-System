    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
            try {
        Class.forName("com.mysql.jdbc.Driver");
        // Establishing database connection
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "root");
        String rollNumber = jTextField1.getText();
        // Check if the roll number exists in the database for deletion
        if (!isRollNumberAlreadyExists(connection, rollNumber)) {
            JOptionPane.showMessageDialog(null, "Student with the given roll number does not exist.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // SQL query to delete data from the 'student' table
            String query = "DELETE FROM student WHERE roll_number=?";

            // Creating a PreparedStatement to safely delete data
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, rollNumber);

            // Executing the delete query
            int rowsAffected = preparedStatement.executeUpdate();

            // Check if the data was deleted successfully
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Student record deleted successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete student record.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

        // Close the database connection
        connection.close();
    } catch (SQLException | ClassNotFoundException e) {
        e.printStackTrace();
    }

    }//GEN-LAST:event_jButton11ActionPerformed

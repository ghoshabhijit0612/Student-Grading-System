private boolean isRollNumberAlreadyExists(Connection connection, String rollNumber) {
    try {
        // SQL query to check if the roll number exists in the 'student' table
        String query = "SELECT * FROM student WHERE roll_number=?";
        PreparedStatement preparedStatement = connection.prepareStatement(query);
        preparedStatement.setString(1, rollNumber);

        // Executing the query
        ResultSet resultSet = preparedStatement.executeQuery();

        // Check if any result is returned, indicating the roll number exists
        return resultSet.next();
    } catch (SQLException e) {
        e.printStackTrace();
        return false; // Return false in case of an exception
    }
}
    
